class ScanResult {
  final File image;
  final List<Map<String, dynamic>> diseasePredictions;
  final List<Map<String, dynamic>> skinTypePredictions;
  final String part;
  final String view;

  ScanResult({
    required this.image,
    required this.diseasePredictions,
    required this.skinTypePredictions,
    required this.part,
    required this.view,
  });

  String get area => '$view $part';

  String get topDiseaseLabel =>
      diseasePredictions.isNotEmpty ? diseasePredictions.first['label'] : 'N/A';
  double get topDiseaseConfidence => diseasePredictions.isNotEmpty
      ? diseasePredictions.first['confidence']
      : 0.0;

  SeverityInfo get severityInfo =>
      SeverityHelper.parseDiseaseLabel(topDiseaseLabel);

  String? get topDiseaseCategory =>
      SkinConditionHelper.getCategory(severityInfo.diseaseName);

  String get topSkinTypeLabel => skinTypePredictions.isNotEmpty
      ? skinTypePredictions.first['label']
      : 'N/A';
  double get topSkinTypeConfidence => skinTypePredictions.isNotEmpty
      ? skinTypePredictions.first['confidence']
      : 0.0;
}

class ScanResultsScreen extends StatefulWidget {
  final List<ScanResult> results;
  final String? userId;

  const ScanResultsScreen({Key? key, required this.results, this.userId})
      : super(key: key);

  @override
  State<ScanResultsScreen> createState() => _ScanResultsScreenState();
}

class _ScanResultsScreenState extends State<ScanResultsScreen> {
  bool _isSaving = false;
  bool _hasSaved = false;
  bool _isGeneratingPdf = false;
  Stream<UserModel?>? _userStream;

  @override
  void initState() {
    super.initState();
    if (widget.userId != null) {
      _userStream =
          context.read<UserRepository>().getUserStream(widget.userId!);
    }
  }

  Future<void> _saveResults(UserModel? user) async {
    if (_isSaving || _hasSaved || user == null) return;

    final currentUser = FirebaseAuth.instance.currentUser;
    if (currentUser == null || currentUser.uid != user.uid) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(
            content: Text('You are not authenticated. Please log in again.'),
            backgroundColor: Colors.red,
          ),
        );
      }
      return;
    }

    setState(() {
      _isSaving = true;
    });

    try {
      final recordRepo = context.read<RecordRepository>();

      // --- Summary Logic ---
      String getUniqueItemsAsString(List<String> items) {
        if (items.isEmpty) return 'N/A';
        return items.toSet().toList().join(', ');
      }

      final allDiseases = getUniqueItemsAsString(
          widget.results.map((r) => r.severityInfo.diseaseName).toList());
      final allSkinTypes = getUniqueItemsAsString(
          widget.results.map((r) => r.topSkinTypeLabel).toList());
      final allClassifications = getUniqueItemsAsString(widget.results
          .map((r) => r.topDiseaseCategory)
          .where((c) => c != null)
          .cast<String>()
          .toList());
      final allSeverities = getUniqueItemsAsString(
          widget.results.map((r) => r.severityInfo.severityText).toList());
      // --- End Summary Logic ---

      final record = DetectionResultModel(
        id: '', // ID will be generated by the repository
        userId: user.uid,
        scanDate: DateTime.now(),
        summary: {
          'skinConditions': allDiseases,
          'skinTypes': allSkinTypes,
          'classifications':
              allClassifications.isEmpty ? 'N/A' : allClassifications,
          'severities': allSeverities,
        },
        imageResults: widget.results.map((r) {
          return ImageResultModel(
            imageUrl: '', // Will be populated during upload
            part: r.part,
            view: r.view,
            diseasePredictions: r.diseasePredictions,
            skinTypePredictions: r.skinTypePredictions,
            localFile: r.image,
            severity: r.severityInfo.severityText,
            severityLevel: r.severityInfo.severity.name,
          );
        }).toList(),
      );

      await recordRepo.saveRecord(record);

      // Play sound on success using a temporary player
      final player = AudioPlayer();
      player.play(AssetSource('sounds/chime.mp3'));

      // Show notification
      if (mounted) {
        final notificationRepository = context.read<NotificationRepository>();
        await notificationRepository.showLocalNotification(
          title: 'Scan Results Saved',
          body:
              'Your scan results have been successfully saved to your profile.',
        );
      }

      setState(() {
        _hasSaved = true;
      });

      if (mounted) {
        // Pop screen and return true to signal success
        Navigator.of(context).pop(true);
      }
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('Failed to save results: $e'),
            backgroundColor: Colors.red,
          ),
        );
      }
    } finally {
      if (mounted) {
        setState(() {
          _isSaving = false;
        });
      }
    }
  }

  Future<void> _generatePdf(UserModel? user) async {
    if (_isGeneratingPdf) return;
    setState(() {
      _isGeneratingPdf = true;
    });

    try {
      final pdfService = PdfService();
      await pdfService.saveResultsAsPdf(widget.results, user);
    } catch (e) {
      // Handle error if needed, maybe with a snackbar
    } finally {
      if (mounted) {
        setState(() {
          _isGeneratingPdf = false;
        });
      }
    }
  }

  @override
  Widget build(BuildContext context) {
    final theme = Theme.of(context);
    return StreamBuilder<UserModel?>(
        stream: _userStream,
        builder: (context, snapshot) {
          final user = snapshot.data;
          return Scaffold(
            appBar: CustomAppBar(
              showBackButton: true,
              title: Text(
                'Scan Results',
                style: theme.textTheme.headlineSmall?.copyWith(
                    fontWeight: FontWeight.bold, color: AppColors.primary),
              ),
              actions: [
                if (user != null && widget.results.isNotEmpty)
                  _isSaving
                      ? const Padding(
                          padding: EdgeInsets.symmetric(
                              horizontal: 4.0, vertical: 8.0),
                          child: SizedBox(
                            width: 24,
                            height: 24,
                            child: CircularProgressIndicator(strokeWidth: 2.0),
                          ),
                        )
                      : GestureDetector(
                          onTap: () => _saveResults(user),
                          child: Tooltip(
                            message: 'Save Results',
                            child: Icon(
                                _hasSaved ? Icons.check_circle : Icons.check),
                          ),
                        ),
              ],
            ),
            floatingActionButton: FloatingActionButton(
              onPressed: () => _generatePdf(user),
              backgroundColor: AppColors.primary,
              elevation: 0,
              highlightElevation: 0,
              shape: const CircleBorder(),
              child: _isGeneratingPdf
                  ? Container(
                      width: 24,
                      height: 24,
                      padding: const EdgeInsets.all(2.0),
                      child: const CircularProgressIndicator(
                        color: Colors.white,
                        strokeWidth: 3,
                      ),
                    )
                  : const Icon(Icons.print, color: Colors.white),
            ),
            body: widget.results.isEmpty
                ? Center(
                    child: Text(
                      'No images were analyzed.',
                      style: theme.textTheme.bodyMedium,
                    ),
                  )
                : SingleChildScrollView(
                    child: Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        Padding(
                          padding: const EdgeInsets.fromLTRB(16, 16, 16, 8),
                          child: Text(
                            'Summary',
                            style: theme.textTheme.headlineSmall?.copyWith(
                              fontWeight: FontWeight.bold,
                              color: theme.colorScheme.primary,
                            ),
                          ),
                        ),
                        _buildSummaryCard(context, user),
                        Padding(
                          padding: const EdgeInsets.fromLTRB(16, 16, 16, 8),
                          child: Text(
                            'Detailed Results',
                            style: theme.textTheme.headlineSmall?.copyWith(
                              fontWeight: FontWeight.bold,
                              color: theme.colorScheme.primary,
                            ),
                          ),
                        ),
                        ListView.separated(
                          shrinkWrap: true,
                          physics: const NeverScrollableScrollPhysics(),
                          padding: EdgeInsets.zero,
                          itemCount: widget.results.length,
                          separatorBuilder: (context, index) =>
                              const Divider(height: 1),
                          itemBuilder: (context, index) {
                            final result = widget.results[index];
                            final severityInfo = result.severityInfo;
                            final description =
                                SkinConditionHelper.getDescription(
                                    severityInfo.diseaseName);
                            final category = result.topDiseaseCategory;

                            return Theme(
                              data: Theme.of(context)
                                  .copyWith(dividerColor: Colors.transparent),
                              child: ExpansionTile(
                                tilePadding:
                                    const EdgeInsets.fromLTRB(16, 8, 16, 8),
                                title: Row(
                                  children: [
                                    ClipRRect(
                                      borderRadius: BorderRadius.circular(12),
                                      child: Image.file(
                                        result.image,
                                        width: 80,
                                        height: 80,
                                        fit: BoxFit.cover,
                                      ),
                                    ),
                                    const SizedBox(width: 16),
                                    Expanded(
                                      child: Column(
                                        crossAxisAlignment:
                                            CrossAxisAlignment.start,
                                        children: [
                                          Text(
                                            result.area,
                                            style: theme.textTheme.bodyMedium
                                                ?.copyWith(
                                              color: theme.colorScheme.onSurface
                                                  .withOpacity(0.7),
                                            ),
                                          ),
                                          if (category != null)
                                            Padding(
                                              padding: const EdgeInsets.only(
                                                  top: 4, bottom: 2),
                                              child: Text(
                                                category.toUpperCase(),
                                                style: theme.textTheme.bodySmall
                                                    ?.copyWith(
                                                  fontWeight: FontWeight.bold,
                                                  letterSpacing: 0.5,
                                                ),
                                              ),
                                            ),
                                          Text(
                                            severityInfo.diseaseName,
                                            style: theme.textTheme.titleLarge
                                                ?.copyWith(
                                              fontWeight: FontWeight.bold,
                                            ),
                                            maxLines: 1,
                                            overflow: TextOverflow.ellipsis,
                                          ),
                                          const SizedBox(height: 4),
                                          Row(
                                            children: [
                                              Container(
                                                padding:
                                                    const EdgeInsets.symmetric(
                                                        horizontal: 8,
                                                        vertical: 4),
                                                decoration: BoxDecoration(
                                                  color: _getSeverityColor(
                                                      severityInfo.severity),
                                                  borderRadius:
                                                      BorderRadius.circular(12),
                                                ),
                                                child: Text(
                                                  severityInfo.severityText,
                                                  style: theme
                                                      .textTheme.bodySmall
                                                      ?.copyWith(
                                                    color: Colors.white,
                                                    fontWeight: FontWeight.bold,
                                                  ),
                                                ),
                                              ),
                                              const SizedBox(width: 8),
                                              Text(
                                                '${(result.topDiseaseConfidence * 100).toStringAsFixed(1)}%',
                                                style: theme
                                                    .textTheme.bodyMedium
                                                    ?.copyWith(
                                                  color: AppColors.primary,
                                                  fontWeight: FontWeight.w600,
                                                ),
                                              ),
                                            ],
                                          ),
                                        ],
                                      ),
                                    ),
                                  ],
                                ),
                                children: [
                                  Padding(
                                    padding:
                                        const EdgeInsets.fromLTRB(24, 8, 24, 8),
                                    child: Column(
                                      crossAxisAlignment:
                                          CrossAxisAlignment.start,
                                      children: [
                                        Container(
                                          padding: const EdgeInsets.all(12),
                                          decoration: BoxDecoration(
                                            color: _getSeverityColor(
                                                    severityInfo.severity)
                                                .withOpacity(0.1),
                                            borderRadius:
                                                BorderRadius.circular(8),
                                            border: Border.all(
                                              color: _getSeverityColor(
                                                  severityInfo.severity),
                                              width: 1,
                                            ),
                                          ),
                                          child: Row(
                                            children: [
                                              Icon(
                                                _getSeverityIcon(
                                                    severityInfo.severity),
                                                color: _getSeverityColor(
                                                    severityInfo.severity),
                                                size: 20,
                                              ),
                                              const SizedBox(width: 8),
                                              Expanded(
                                                child: Text(
                                                  SeverityHelper
                                                      .getSeverityDescription(
                                                          severityInfo
                                                              .severity),
                                                  style:
                                                      theme.textTheme.bodySmall,
                                                ),
                                              ),
                                            ],
                                          ),
                                        ),
                                        const Divider(height: 24),
                                        if (description != null) ...[
                                          Text(
                                            'Description',
                                            style: theme.textTheme.titleMedium
                                                ?.copyWith(
                                                    fontWeight:
                                                        FontWeight.bold),
                                          ),
                                          const SizedBox(height: 4),
                                          Text(description,
                                              style:
                                                  theme.textTheme.bodyMedium),
                                          const Divider(height: 24),
                                        ],
                                        _buildPredictionList(
                                          context,
                                          'Top Disease Predictions',
                                          result.diseasePredictions,
                                        ),
                                        const Divider(height: 24),
                                        _buildPredictionList(
                                          context,
                                          'Skin Type Predictions',
                                          result.skinTypePredictions,
                                        ),
                                        const SizedBox(height: 8),
                                      ],
                                    ),
                                  ),
                                ],
                              ),
                            );
                          },
                        ),
                        Padding(
                          padding: const EdgeInsets.all(16.0),
                          child: Text(
                            'Disclaimer: The results from this scan are not a substitute for professional medical advice, diagnosis, or treatment. Always seek the advice of your physician or other qualified health provider with any questions you may have regarding a medical condition.',
                            style: theme.textTheme.bodySmall
                                ?.copyWith(color: Colors.grey[600]),
                            textAlign: TextAlign.center,
                          ),
                        ),
                      ],
                    ),
                  ),
          );
        });
  }

  Color _getSeverityColor(SeverityLevel level) {
    switch (level) {
      case SeverityLevel.harmless:
        return Colors.green;
      case SeverityLevel.mild:
        return Colors.blue;
      case SeverityLevel.moderate:
        return Colors.orange;
      case SeverityLevel.severe:
        return Colors.red;
      case SeverityLevel.unknown:
        return Colors.grey;
    }
  }

  IconData _getSeverityIcon(SeverityLevel level) {
    switch (level) {
      case SeverityLevel.harmless:
        return Icons.check_circle;
      case SeverityLevel.mild:
        return Icons.info;
      case SeverityLevel.moderate:
        return Icons.warning;
      case SeverityLevel.severe:
        return Icons.error;
      case SeverityLevel.unknown:
        return Icons.help;
    }
  }

  Widget _buildSummaryCard(BuildContext context, UserModel? user) {
    final theme = Theme.of(context);

    String getUniqueItemsAsString(List<String> items) {
      if (items.isEmpty) return 'N/A';
      final uniqueItems = items.toSet().toList();
      return uniqueItems.join(', ');
    }

    final allDiseases = getUniqueItemsAsString(
        widget.results.map((r) => r.severityInfo.diseaseName).toList());
    final allSkinTypes = getUniqueItemsAsString(
        widget.results.map((r) => r.topSkinTypeLabel).toList());
    final allClassifications = getUniqueItemsAsString(widget.results
        .map((r) => r.topDiseaseCategory)
        .where((c) => c != null)
        .cast<String>()
        .toList());
    final allSeverities = getUniqueItemsAsString(
        widget.results.map((r) => r.severityInfo.severityText).toList());

    final scanDate = DateFormat('MM/dd/yyyy').format(DateTime.now());

    return Padding(
      padding: const EdgeInsets.fromLTRB(16, 0, 16, 0),
      child: Row(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Expanded(
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                _buildSummaryRow(context, 'Skin Condition(s):', allDiseases),
                const SizedBox(height: 8),
                _buildSummaryRow(context, 'Severity Level(s):', allSeverities),
                const SizedBox(height: 8),
                _buildSummaryRow(context, 'Skin Type(s):', allSkinTypes),
                const SizedBox(height: 8),
                _buildSummaryRow(context, 'Classification(s):',
                    allClassifications.isEmpty ? 'N/A' : allClassifications),
              ],
            ),
          ),
          const SizedBox(width: 16),
          Column(
            crossAxisAlignment: CrossAxisAlignment.end,
            children: [
              Text(
                '${user?.firstName ?? ''} ${user?.lastName ?? ''}'.trim(),
                style: theme.textTheme.titleLarge
                    ?.copyWith(fontWeight: FontWeight.bold),
              ),
              const SizedBox(height: 4),
              Text(scanDate, style: theme.textTheme.bodyMedium),
              const SizedBox(height: 4),
              Text('${widget.results.length} images uploaded',
                  style: theme.textTheme.bodyMedium),
            ],
          ),
        ],
      ),
    );
  }

  Widget _buildSummaryRow(BuildContext context, String label, String value) {
    final theme = Theme.of(context);
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Text(label,
            style: theme.textTheme.bodyMedium?.copyWith(color: Colors.grey)),
        Text(value, style: theme.textTheme.titleMedium, softWrap: true),
      ],
    );
  }

  Widget _buildPredictionList(BuildContext context, String title,
      List<Map<String, dynamic>> predictions) {
    final theme = Theme.of(context);
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Text(
          title,
          style: theme.textTheme.titleMedium?.copyWith(
            fontWeight: FontWeight.bold,
          ),
        ),
        const SizedBox(height: 8),
        ...predictions.take(3).map((p) {
          return Padding(
            padding: const EdgeInsets.only(bottom: 4.0),
            child: Row(
              mainAxisAlignment: MainAxisAlignment.spaceBetween,
              children: [
                Text(p['label'], style: theme.textTheme.bodyMedium),
                Text(
                  '${(p['confidence'] * 100).toStringAsFixed(1)}%',
                  style: theme.textTheme.bodyMedium?.copyWith(
                    color: AppColors.primary,
                  ),
                ),
              ],
            ),
          );
        }),
      ],
    );
  }
}
